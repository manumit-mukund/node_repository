let num1 = 5;

let num2 = "5";

console.log('(num1 != num2) = ', (num1 != num2)); // just the reverse of ==

//The !== operator in JavaScript is a strict inequality operator. 
// It compares whether two values are not equal to each other, both in terms of value and type. 
// It returns true if the values are not equal or if they are of different types
console.log('\n(num1 !== num2) = ', (num1 !== num2)); 