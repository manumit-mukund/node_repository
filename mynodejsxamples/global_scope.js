let x = 2;
const y = 3;
var z = 4;

function myFunction() {

    console.log('----------------------Inside the function--------------------------------------');

    console.log('x = ' + x);

    console.log('\ny = ' + y);

    console.log('\nz = ' + z);
}

myFunction();

console.log('\n----------------------Outside the function--------------------------------------');

console.log('\nx = ' + x);

console.log('\ny = ' + y);

console.log('\nz = ' + z);

