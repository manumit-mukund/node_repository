console.log('for loop using let');

for (let i = 0; i < 5; i++) {

  console.log(i);

}

console.log('\nfor loop using var');

for (var i = 0; i < 5; i++) {

  console.log(i);

}

console.log('\nfor loop without variable type');

for (i = 0; i < 5; i++) {

  console.log(i);

}

console.log('\nfor loop using const');

for (const i = 0; i < 5; i++) {

  console.log(i);

}